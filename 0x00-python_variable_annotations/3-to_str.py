#!/usr/bin/env python3
"""
Type-annotated function to_str that
takes a float n as argument and
returns the string representation of the float.
"""


def to_str(n: float) -> str:
    """
    Get the string representation of a float

    Args:
        n (float): Number from which we must obtain its representation

    Returns:
        str: String representation of the number
    """
    return (str(n))
