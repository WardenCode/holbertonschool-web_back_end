#!/usr/bin/env python3
"""
Write a type-annotated function add that takes
a float a and a float b as arguments and
returns their sum as a float.
"""


def add(a: float, b: float) -> float:
    """
    Execute an addition of float numbers

    Args:
        a (float): First number for the addition
        b (float): Second number for the addition

    Returns:
        float: The addition of a and b (a + b)
    """
    return (a + b)
